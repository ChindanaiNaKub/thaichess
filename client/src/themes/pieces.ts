export type PieceThemeRendering = 'vector' | 'sprite';

export interface TraditionalPiecePalette {
  fillTop: string;
  fillBase: string;
  fillBottom: string;
  stroke: string;
  shadow: string;
  promotedDot: string;
}

export type PieceSpriteSet = Record<PieceColor, Record<PieceType, string>>;

export interface PieceThemeConfig {
  id: PieceThemeId;
  label: string;
  description: string;
  rendering: PieceThemeRendering;
  colors?: Record<PieceColor, TraditionalPiecePalette>;
  sprites?: PieceSpriteSet;
}

